#Type Conversion
#แบบฝึกหัด 1-5 ข้อที่ 1
x = [1,2,3]
y = [4,5,6]
# แปลง x ให้เป็นสตริง และ
# นำมาต่อด้วยสตริงที่ได้จาก
# การแปลง y ให้เป็นสตริง 
# เก็บผลลัพธืในตัวแป

x = [1,2,3]
y = [4,5,6]
